def insert_data(engine: Engine, df: pd.DataFrame):
    # open_time and close_time represent in milliseconds
    df.open_time = df.open_time.apply(
        lambda x: datetime.fromtimestamp(int(x) / 1000)
    )
    df.close_time = df.close_time.apply(
        lambda x: datetime.fromtimestamp(int(x) / 1000)
    )
    df.to_sql(name="crypto", con=engine, if_exists='append', index=False)
